# Print statements are used to display information to the console
print("Hello, World!")

# You can use the print function to print multiple functions
print("The sum of", 1, "and", 1, "equals", 1 + 1)

# You can add an 'f' string instead of adding commas to simplify your print-out. Use {} to encapsulate the function you want to print.
name = "Bob"
age = 27
print(f"My name is {name}, and I am {age} years old.")